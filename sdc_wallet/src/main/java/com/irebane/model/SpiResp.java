package com.irebane.model;

import java.util.List;
import java.util.Map;

/**
 * 统一返回实体 定义了一个errorCode来表示返回情况 定义了一个errorMsg来表示返回提醒 定义了两个基本容器Map和List
 * 
 * @author gegl
 * @date 2016-10-1 下午03:14:19
 */
public class SpiResp implements java.io.Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String errorCode;
	private String errorMsg;
	private Map m;
	private List l;
	private int total;

	public String getErrorCode() {
		return errorCode;
	}

	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

	public Map getM() {
		return m;
	}

	public void setM(Map m) {
		this.m = m;
	}

	public List getL() {
		return l;
	}

	public void setL(List l) {
		this.l = l;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	@Override
	public String toString() {
		return "SpiResp [errorCode=" + errorCode + ", errorMsg=" + errorMsg
				+ ", m=" + m + ", l=" + l + ", total=" + total + "]";
	}

}
